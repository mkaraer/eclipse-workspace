package day19_loops02;

public class slowlyPrintMyNumbers {
	public static void main (String[] args) throws InterruptedException  {
//		write programs
		
		int num=1;
		while(num<=10) {
			System.out.print(num+ " ");
			num++;  // add one to num
			Thread.sleep(2000); // speed.... moves slowly
		}
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}
}
